//! Prompt 常量 — A 股特化

/// A股交易制度核心约束（必须注入所有分析Prompt）
pub const A_STOCK_CONSTRAINTS_PROMPT: &str = r#"
## A股交易制度核心约束（必须遵守）

1. **T+1结算制度**：当日买入的股票，必须等到下一个交易日才能卖出。这是A股与港股/美股最本质的区别。
   - 警告：不要建议"日内高抛低吸"，这在A股是不可能的。
   - 如果看到"盘中回调买入"，必须提醒"T+1锁仓风险"。

2. **涨跌停限制**：
   - 主板：±10%，创业板/科创板：±20%，ST股：±5%，新股上市首日无限制（主板44%/创业板/科创板无限制）。
   - 涨停价买入：排队可能排不上，跌停价卖出：排队可能卖不掉。
   - 警告：如果推荐涨停股买入，必须提醒"封板强度"和"炸板风险"。

3. **ST/退市风险**：
   - ST股连续20个交易日收盘价低于1元触发退市。
   - ST股涨跌幅限制为5%，但退市整理期为15天，期间涨跌幅限制为±10%。
   - 警告：如果分析对象为ST股，必须明确标注退市风险。

4. **北向资金影响**：
   - 北向资金单日净流入超100亿或净流出超80亿，通常预示大盘方向。
   - 北向资金偏好大盘蓝筹，与游资偏好小盘股形成对比。

5. **最小交易单位**：A股最小交易单位为100股（1手），买入必须是100的整数倍。

6. **交易时段**：9:30-11:30, 13:00-15:00，集合竞价9:15-9:25。
"#;

