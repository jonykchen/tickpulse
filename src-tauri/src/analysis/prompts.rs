/// 维度分析提示词
pub fn dimension_prompt(dimension: &str, stock_name: &str, data: &str) -> String {
    format!(
        r#"## 分析任务
请对 **{}** 进行「{}」维度分析。

## 股票数据
{}

## 分析要求
1. 基于上述数据进行深入分析
2. 给出明确的评级和理由
3. 列出关键风险和机会
4. 评估分析置信度
5. 注意A股市场特殊性

请以 JSON 格式输出分析结果。"#,
        stock_name, dimension, data
    )
}

