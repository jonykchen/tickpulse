/// 各维度必须采集的数据项，无法获取时标注[数据缺失: xxx]
pub fn get_mandatory_checklist(dimension: &str) -> Vec<&'static str> {
    match dimension {
        "Technical" => vec!["分时走势", "量价关系", "换手率", "量比"],
        "News" => vec!["最近3天相关新闻", "政策动态"],
        "Fundamentals" => vec!["最新财报", "ROE", "净利润增速"],
        "Policy" => vec!["行业政策", "监管动态"],
        "HotMoney" => vec!["龙虎榜数据", "游资席位", "换手率"],
        "Lockup" => vec!["解禁时间表", "解禁比例"],
        "Peg" => vec!["PE(TTM)", "业绩增速(CAGR)", "PEG值"],
        _ => vec![],
    }
}
